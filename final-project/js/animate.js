$(document).ready(function () {
  $('#web-name').textillate({
      // Set the type of animation
      in: {
          effect: 'fadeInLeft', // Choose the animation effect
          delayScale: 1.5,      // Timing of the delay between letters
          delay: 50             // Delay between each letter animation
      },
      loop: true // Set to true if you want it to loop, false otherwise
  });
});


